pub(crate) fn days_in_month(year: i32, month0: u32) -> Option<u32> {
    if is_leap_year(year) {
        MONTH_TO_YEARDAYS_LEAPYEAR
            .get(month0 as usize)
            .map(|x| x.end - x.start)
    } else {
        MONTH_TO_YEARDAYS_NORMAL
            .get(month0 as usize)
            .map(|x| x.end - x.start)
    }
}
